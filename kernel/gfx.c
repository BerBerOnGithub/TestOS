/*
 * gfx.c  —  VGA Mode 13h graphics driver
 *
 * Mode 13h is entered via the BIOS (INT 10h, AX=0x0013) early in boot,
 * before we switch to protected mode.  Here we just program the palette
 * and provide pixel/line/rect/text primitives.
 *
 * The BIOS call is issued from a small real-mode trampoline we park
 * at 0x7E00 and jump to from protected mode via a temporary switch.
 * Simpler alternative used here: directly program VGA registers.
 */

#include "gfx.h"
#include "../libc/klib.h"
#include <stdint.h>

/* ── Port I/O ──────────────────────────────────────────────── */
static inline void outb(uint16_t p, uint8_t v){ __asm__ volatile("outb %0,%1"::"a"(v),"Nd"(p)); }
static inline uint8_t inb(uint16_t p){ uint8_t v; __asm__ volatile("inb %1,%0":"=a"(v):"Nd"(p)); return v; }

/* ── VGA register sets for mode 13h ────────────────────────── */
/* These are the standard VGA register values for 320x200x256 */

static const uint8_t seq_regs[5]  = { 0x03,0x01,0x0F,0x00,0x0E };
static const uint8_t crtc_regs[25] = {
    0x5F,0x4F,0x50,0x82,0x54,0x80,0xBF,0x1F,
    0x00,0x41,0x00,0x00,0x00,0x00,0x00,0x00,
    0x9C,0x0E,0x8F,0x28,0x40,0x96,0xB9,0xA3,0xFF
};
static const uint8_t gc_regs[9]   = { 0x00,0x00,0x00,0x00,0x00,0x40,0x05,0x0F,0xFF };
static const uint8_t ac_regs[21]  = {
    0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,
    0x08,0x09,0x0A,0x0B,0x0C,0x0D,0x0E,0x0F,
    0x41,0x00,0x0F,0x00,0x00
};

static void write_regs(void) {
    /* Misc output */
    outb(0x3C2, 0x63);

    /* Sequencer */
    for (int i = 0; i < 5; i++) { outb(0x3C4, (uint8_t)i); outb(0x3C5, seq_regs[i]); }

    /* CRTC: unlock, write, relock */
    outb(0x3D4, 0x03); outb(0x3D5, inb(0x3D5) | 0x80);
    outb(0x3D4, 0x11); outb(0x3D5, inb(0x3D5) & ~0x80);
    for (int i = 0; i < 25; i++) { outb(0x3D4, (uint8_t)i); outb(0x3D5, crtc_regs[i]); }

    /* Graphics controller */
    for (int i = 0; i < 9; i++) { outb(0x3CE, (uint8_t)i); outb(0x3CF, gc_regs[i]); }

    /* Attribute controller */
    inb(0x3DA);  /* reset flip-flop */
    for (int i = 0; i < 21; i++) {
        outb(0x3C0, (uint8_t)i);
        outb(0x3C0, ac_regs[i]);
    }
    outb(0x3C0, 0x20);  /* enable display */
}

