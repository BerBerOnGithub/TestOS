#define PALETTE_ENTRIES (sizeof(palette_data)/sizeof(palette_data[0]))

static void load_palette(void) {
    outb(0x3C8, 0);
    for (uint32_t i = 0; i < 256; i++) {
        if (i < PALETTE_ENTRIES) {
            outb(0x3C9, palette_data[i][0]);
            outb(0x3C9, palette_data[i][1]);
            outb(0x3C9, palette_data[i][2]);
        } else {
            outb(0x3C9, 0); outb(0x3C9, 0); outb(0x3C9, 0);
        }
    }
}

