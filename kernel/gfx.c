void gfx_init(void) {
    write_regs();
    load_palette();
    gfx_clear(COL_BLACK);
}

void gfx_text_mode(void) {
    /* Mode 3: 80x25 text */
    /* We need BIOS for this — use a real-mode trampoline */
    /* For now, just re-init VGA text registers manually */
    /* (Full implementation would switch back via BIOS INT 10h) */
    /* In practice REBOOT is used to exit GUI, so this is fine */
    (void)0;
}

void gfx_clear(uint8_t col) {
    kmemset((void*)GFX_BUF, col, GFX_W * GFX_H);
}

void gfx_pixel(int x, int y, uint8_t col) {
    if (x < 0 || x >= GFX_W || y < 0 || y >= GFX_H) return;
    GFX_BUF[y * GFX_W + x] = col;
}

uint8_t gfx_get_pixel(int x, int y) {
    if (x < 0 || x >= GFX_W || y < 0 || y >= GFX_H) return 0;
    return GFX_BUF[y * GFX_W + x];
}

void gfx_hline(int x, int y, int w, uint8_t col) {
    if (y < 0 || y >= GFX_H) return;
    if (x < 0) { w += x; x = 0; }
    if (x + w > GFX_W) w = GFX_W - x;
    if (w <= 0) return;
    kmemset((void*)(GFX_BUF + y*GFX_W + x), col, (uint32_t)w);
}

void gfx_vline(int x, int y, int h, uint8_t col) {
    for (int i = 0; i < h; i++) gfx_pixel(x, y+i, col);
}

void gfx_rect(int x, int y, int w, int h, uint8_t col) {
    gfx_hline(x,     y,     w, col);
    gfx_hline(x,     y+h-1, w, col);
    gfx_vline(x,     y,     h, col);
    gfx_vline(x+w-1, y,     h, col);
}

void gfx_fill_rect(int x, int y, int w, int h, uint8_t col) {
    for (int r = 0; r < h; r++) gfx_hline(x, y+r, w, col);
}

void gfx_char(int x, int y, char c, uint8_t fg, uint8_t bg) {
    if (c < 0x20 || c > 0x7E) c = '?';
    const uint8_t *glyph = font8x8[(int)(c - 0x20)];
    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            uint8_t pix = (bits & (0x80 >> col)) ? fg : bg;
            gfx_pixel(x+col, y+row, pix);
        }
    }
}

void gfx_str(int x, int y, const char *s, uint8_t fg, uint8_t bg) {
    int cx = x;
    while (*s) {
        if (*s == '\n') { cx = x; y += 9; }
        else { gfx_char(cx, y, *s, fg, bg); cx += 8; }
        s++;
    }
}

void gfx_str_transparent(int x, int y, const char *s, uint8_t fg) {
    int cx = x;
    while (*s) {
        if (*s == '\n') { cx = x; y += 9; }
        else {
            const uint8_t *glyph = font8x8[(*s >= 0x20 && *s <= 0x7E) ? (*s - 0x20) : 0];
            for (int row = 0; row < 8; row++) {
                uint8_t bits = glyph[row];
                for (int col = 0; col < 8; col++)
                    if (bits & (0x80 >> col)) gfx_pixel(cx+col, y+row, fg);
            }
            cx += 8;
        }
        s++;
    }
}

void gfx_save_region(int x, int y, int w, int h, uint8_t *dst) {
    for (int r = 0; r < h; r++) {
        if (y+r < 0 || y+r >= GFX_H) continue;
        int cx = x < 0 ? 0 : x;
        int cw = w; if (cx+cw > GFX_W) cw = GFX_W-cx;
        for (int c = 0; c < cw; c++) dst[r*w + (cx-x) + c] = GFX_BUF[(y+r)*GFX_W+cx+c];
    }
}

void gfx_restore_region(int x, int y, int w, int h, const uint8_t *src) {
    for (int r = 0; r < h; r++) {
        if (y+r < 0 || y+r >= GFX_H) continue;
        int cx = x < 0 ? 0 : x;
        int cw = w; if (cx+cw > GFX_W) cw = GFX_W-cx;
        for (int c = 0; c < cw; c++) GFX_BUF[(y+r)*GFX_W+cx+c] = src[r*w+(cx-x)+c];
    }
}

void gfx_blit(int dx, int dy, int w, int h, const uint8_t *src) {
    for (int r = 0; r < h; r++)
        for (int c = 0; c < w; c++)
            gfx_pixel(dx+c, dy+r, src[r*w+c]);
}

void gfx_raised_box(int x, int y, int w, int h) {
    gfx_fill_rect(x, y, w, h, COL_BTN_FACE);
    gfx_hline(x,     y,     w, COL_BTN_HI);   /* top    */
    gfx_vline(x,     y,     h, COL_BTN_HI);   /* left   */
    gfx_hline(x,     y+h-1, w, COL_BTN_SH);   /* bottom */
    gfx_vline(x+w-1, y,     h, COL_BTN_SH);   /* right  */
}

void gfx_sunken_box(int x, int y, int w, int h) {
    gfx_fill_rect(x, y, w, h, COL_WIN_BG);
    gfx_hline(x,     y,     w, COL_BTN_SH);
    gfx_vline(x,     y,     h, COL_BTN_SH);
    gfx_hline(x,     y+h-1, w, COL_BTN_HI);
    gfx_vline(x+w-1, y,     h, COL_BTN_HI);
}
