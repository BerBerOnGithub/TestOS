; ===========================================================================
; pm/font.asm - Bitmap font renderer + embedded CP437 8x8 font data
;
; No BIOS required. Font data baked directly into the binary.
; Works on any hardware, real mode or PM.
;
; Public interface (32-bit PM):
;
;   fb_draw_char
;     In: AL  = ASCII character
;         EBX = x (pixels)
;         ECX = y (pixels)
;         DL  = foreground colour index
;         DH  = background colour index (0xFF = transparent)
;
;   fb_draw_string
;     In: ESI = pointer to null-terminated string
;         EBX = x (pixels)
;         ECX = y (pixels)
;         DL  = foreground colour
;         DH  = background colour (0xFF = transparent)
;     Out: EBX = x after last character
; ===========================================================================

[BITS 32]

; -
; fb_draw_char
; Writes directly to framebuffer " no fb_draw_pixel calls
; In: AL=char, EBX=x, ECX=y, DL=fg, DH=bg (0xFF=transparent)
; -
fb_draw_char:
    ; bounds check
    cmp   ebx, 0
    jl    .ret
    cmp   ebx, 632           ; 640 - 8
    jg    .ret
    cmp   ecx, 0
    jl    .ret
    cmp   ecx, 472           ; 480 - 8
    jg    .ret
    
    push eax
    push ebx
    push ecx
    push edx
    push esi
    push edi
    jmp   .start
.ret:
    ret

.start:

    ; compute glyph pointer: font_data + (char & 0x7F)*8
    and   eax, 0x7F          ; Limit to first 128 characters
    shl   eax, 3
    add   eax, font_data
    mov   [fc_glyph], eax

    mov   [fc_x],  ebx
    mov   [fc_y],  ecx
    mov   [fc_fg], dl
    mov   [fc_bg], dh

    ; cache framebuffer base and pitch in registers for the row loop
    mov   ebx, [gfx_fb_base]
    mov   ecx, [gfx_fb_pitch]

    ; row loop: esi = row index (0..7)
    xor   esi, esi
.row:
    cmp   esi, 8
    jge   .char_done

    ; EDI = framebuffer address of pixel (fc_x, fc_y + row)
    ; EDI = gfx_fb_base + (fc_y + row) * gfx_fb_pitch + fc_x
    mov   edi, ebx
    mov   eax, [fc_y]
    add   eax, esi
    mul   ecx                    ; eax = (fc_y+row) * pitch
    add   edi, eax
    add   edi, [fc_x]            ; edi = start of this char row in fb

    ; load bitmap byte for this row
    mov   eax, [fc_glyph]
    movzx eax, byte [eax + esi]  ; al = bitmap
    mov   [fc_bits], al

    ; col loop: edx = col index (0..7)
    xor   edx, edx
.col:
    cmp   edx, 8
    jge   .next_row

    ; test bit 7-col (MSB = leftmost)
    movzx eax, byte [fc_bits]
    push  eax
    mov   eax, 7
    sub   eax, edx
    bt    dword [esp], eax       ; CF=1 if pixel set
    add   esp, 4

    jc    .fg_px

    ; background
    cmp   byte [fc_bg], 0xFF
    je    .next_col
    mov   al, [fc_bg]
    mov   [edi + edx], al
    jmp   .next_col

.fg_px:
    mov   al, [fc_fg]
    mov   [edi + edx], al

.next_col:
    inc   edx
    jmp   .col

.next_row:
    inc   esi
    jmp   .row

.char_done:
    pop  edi
    pop  esi
    pop  edx
    pop  ecx
    pop  ebx
    pop  eax
    ret

; -
; fb_draw_string
; -
fb_draw_string:
    push eax
    push ecx
    push edx
    push esi

.loop:
    mov   al, [esi]
    test  al, al
    jz    .done
    call  fb_draw_char
    add   ebx, 8
    inc   esi
    jmp   .loop

.done:
    pop  esi
    pop  edx
    pop  ecx
    pop  eax
    ret

